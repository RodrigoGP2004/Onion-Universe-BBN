// INTEGRATION

int gaussian_elimination(double A[N_EQ][N_EQ], double b[N_EQ], double x[N_EQ]) {
    double mat[N_EQ][N_EQ+1];
    for(int i=0; i<N_EQ; i++) {
        for(int j=0; j<N_EQ; j++) mat[i][j] = A[i][j];
        mat[i][N_EQ] = b[i];
    }
    for(int i=0; i<N_EQ; i++) {
        int max_row = i;
        for(int k=i+1; k<N_EQ; k++) 
            if(fabs(mat[k][i]) > fabs(mat[max_row][i])) max_row = k;
            
        if(fabs(mat[max_row][i]) < 1e-30) return 0; 

        for(int k=i; k<=N_EQ; k++) {
            double temp = mat[i][k];
            mat[i][k] = mat[max_row][k];
            mat[max_row][k] = temp;
        }
        for(int k=i+1; k<N_EQ; k++) {
            double factor = mat[k][i] / mat[i][i];
            for(int j=i; j<=N_EQ; j++) mat[k][j] -= factor * mat[i][j];
        }
    }
    for(int i=N_EQ-1; i>=0; i--) {
        x[i] = mat[i][N_EQ];
        for(int j=i+1; j<N_EQ; j++) x[i] -= mat[i][j] * x[j];
        x[i] /= mat[i][i];
    }
    return 1;
}

int implicit_step(double t, const double Y[], double dt, double Y_out[], double J[N_EQ][N_EQ], int calc_jac) {
    double f0[N_EQ], f1[N_EQ], Y_temp[N_EQ];
    double A[N_EQ][N_EQ], b[N_EQ], delta_Y[N_EQ];
    
    bbn_network(t, Y, f0);

    if (calc_jac) {
        for (int j = 0; j < N_EQ; j++) {
            for (int i = 0; i < N_EQ; i++) Y_temp[i] = Y[i];
            double h = fabs(Y[j]) * 1e-6 + 1e-15; 
            Y_temp[j] += h;
            bbn_network(t, Y_temp, f1);
            for (int i = 0; i < N_EQ; i++) J[i][j] = (f1[i] - f0[i]) / h;
        }
    }

    for(int i=0; i<N_EQ; i++) {
        for(int j=0; j<N_EQ; j++) {
            A[i][j] = -dt * J[i][j];
            if(i == j) A[i][j] += 1.0; 
        }
        b[i] = dt * f0[i];
    }

    if(!gaussian_elimination(A, b, delta_Y)) return 0;

    for(int i=0; i<N_EQ; i++) Y_out[i] = Y[i] + delta_Y[i];
    return 1;
}

