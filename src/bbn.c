double g_star_func(double T_MeV) {
    double x = 3.0 * log(T_MeV / 0.18);
    return g_star_0 + (g_star_i - g_star_0) * 0.5 * (1.0 + tanh(x)); 
}

double g_star_s_func(double T_MeV) {
    double x = 3.0 * log(T_MeV / 0.18);
    return g_star_s_0 + (g_star_i - g_star_s_0) * 0.5 * (1.0 + tanh(x));
}

double T_interp(double t_target) {
    if (t_target <= t_array[0]) return T_array[0];
    if (t_target >= t_array[N_TEMP-1]) return T_array[N_TEMP-1];
    
    static int i = 0;
    if (t_target < t_array[i]) i = 0;
    
    while (i < N_TEMP - 2 && t_array[i+1] < t_target) i++;
    
    double log_t0 = log(t_array[i]);
    double log_t1 = log(t_array[i+1]);
    double log_T0 = log(T_array[i]);
    double log_T1 = log(T_array[i+1]);
    
    return exp(log_T0 + (log(t_target) - log_t0) * (log_T1 - log_T0) / (log_t1 - log_t0));
}

// Cosmology

double Time_Onion(double Temp) {
    double H0_s_local = H0_km_s_Mpc_Onion * km_to_m / Mpc_to_m;
    double TCurv = 1.0 / (H0_s_local * sqrt(Omega_L0));
    double t0 = 0.5 * TCurv * log((1.0 + sqrt(Omega_L0)) / (1.0 - sqrt(Omega_L0)));
    return TCurv * asinh(T0 * sinh(t0 / TCurv) / Temp);
}

double H_LCDM(double lnT) {
    double T = exp(lnT);
    double ratio = T / T0;
    double r2 = ratio * ratio;
    double r3 = r2 * ratio;
    double r4 = r2 * r2;
    double g_star_ratio = g_star_func(T) / g_star_0; 
    double g_star_s_ratio = g_star_s_func(T) / g_star_s_0;
    return H0_s * sqrt(Omega_r0 * g_star_ratio * r4 + Omega_m0 * g_star_s_ratio * r3 + Omega_k0 * r2 + Omega_L0);
}

double H_Onion(double lnT) {
    double T = exp(lnT);
    double ratio = T / T0;
    return H0_s * sqrt(-Omega_k0 * (ratio * ratio) + Omega_L0);
}

double integrand_t(double lnT, double (*H_func)(double)) { 
    double T = exp(lnT);
    double H = H_func(lnT);
    
    double x = 3.0 * log(T / 0.18);
    double th = tanh(x);
    
    double dg_dlnT = (g_star_i - g_star_s_0) * 0.5 * 3.0 * (1.0 - th * th);
    double g_s = g_star_s_0 + (g_star_i - g_star_s_0) * 0.5 * (1.0 + th);
    
    double dln_g_dln_T = dg_dlnT / g_s;
    
    return (1.0 + dln_g_dln_T / 3.0) / H; 
}

double integrate_simpson(double a, double b, int n, double (*H_func)(double)) {
    if (n % 2 != 0) n++; 
    double h = (b - a) / n;
    double sum = integrand_t(a, H_func) + integrand_t(b, H_func);
    for (int i = 1; i < n; i++) sum += integrand_t(a + i * h, H_func) * (i % 2 == 0 ? 2.0 : 4.0);
    return sum * h / 3.0;
}

double T_dec(double (*H_func)(double)) {
    double a = 0.001, b = 50.0, c;
    while ((b - a) >= 1e-5) {
        c = (a + b) / 2.0;
        double f_c = (Gamma_np(c) - 1/tau_n) - H_func(log(c));
        double f_a = (Gamma_np(a) - 1/tau_n) - H_func(log(a));
        if (f_c == 0.0) break;
        if (f_c * f_a < 0) b = c; else a = c;
    }
    return c;
}

// Nuclear reactions

double jina_dict(double T9, int n_sets, double p[][7]) {
    double rate = 0.0;
    double T9_safe = fmax(T9, 1e-9);
    double t13 = cbrt(T9_safe);
    double t53 = T9_safe * t13 * t13;
    double lnt9 = log(T9_safe);
    
    for (int i = 0; i < n_sets; i++) {
        double exp_val = p[i][0] + p[i][1]/T9_safe + p[i][2]/t13 + 
                         p[i][3]*t13 + p[i][4]*T9_safe + p[i][5]*t53 + p[i][6]*lnt9;
        if (exp_val > 100.0) exp_val = 100.0; 
        if (exp_val > -700.0) rate += exp(exp_val);
    }
    return rate;
}

double rho_molar(double T_MeV) {
    double T_term = T_MeV / hbar_c_MeV_cm;
    double n_gamma = (2.0 * zetta_3) / (M_PI * M_PI) * (T_term * T_term * T_term);
    double n_b = eta * n_gamma * (g_star_s_func(T_MeV) / g_star_s_0);
    return n_b / N_Avogadro;
}

