double f_FD(double E, double T) {
    if (E / T > 700.0) return 0.0; 
    return 1.0 / (1.0 + exp(E / T));
}

double fermi_func(double E_e, double p_e, int Z) {
    if (p_e < 1e-5) return 1.0;
    double alpha = 1.0 / 137.035999;
    double eta_c = alpha * Z * E_e / p_e;
    double term = 2.0 * M_PI * eta_c;
    
    if (term > 50.0) return term;
    if (term < -50.0) return 0.0;      
    if (fabs(term) < 1e-4) return 1.0 + term / 2.0; 
    
    return term / (1.0 - exp(-term));
}

double I_vac_func() {
    double a = m_e, b = Qnp, sum = 0.0, h = (b - a) / 500.0;
    for (int i = 0; i <= 500; i++) {
        double E_e = a + i * h;
        double p_e = (E_e > m_e) ? sqrt(E_e * E_e - m_e * m_e) : 0.0;
        double E_nu = Qnp - E_e;
        double valor = E_e * p_e * E_nu * E_nu * fermi_func(E_e, p_e, 1);
        double peso = (i == 0 || i == 500) ? 1.0 : ((i % 2 != 0) ? 4.0 : 2.0);
        sum += peso * valor;
    }
    return sum * (h / 3.0);
}

double T_nu_part(double T_gamma) {
    if (modelo == 1) return T_gamma; 
    double ratio_high = 1.0;
    double ratio_low = 0.7137658555; // cbrt(4/11)
    double ratio = ratio_low + (ratio_high - ratio_low) * 0.5 * (1.0 + tanh((T_gamma - T_ee) / 0.15));
    return T_gamma * ratio;
}

double Gamma_np(double T) {
    if (T < 0.001) return 1.0 / tau_n;
    double T_nu = T_nu_part(T);
    static double I_vac = 0.0;
    if (I_vac == 0.0) I_vac = I_vac_func();

    double sum_total = 0.0, a, b, h, sum, E_e, p_e, E_nu, val, peso;
    int n = 500;

    // 1. n -> p + e- + nu_bar
    a = m_e; b = Qnp; h = (b - a) / n; sum = 0.0;
    for (int i = 0; i <= n; i++) {
        E_e = a + i * h;
        p_e = (E_e > m_e) ? sqrt(E_e * E_e - m_e * m_e) : 0.0;
        E_nu = Qnp - E_e;
        val = E_e * p_e * E_nu * E_nu * (1.0 - f_FD(E_e, T)) * (1.0 - f_FD(E_nu, T_nu)) * fermi_func(E_e, p_e, 1);
        peso = (i == 0 || i == n) ? 1.0 : ((i % 2 != 0) ? 4.0 : 2.0);
        sum += peso * val;
    }
    sum_total += (sum * h / 3.0);

    // 2. e+ + n -> p + nu_bar
    a = m_e; b = m_e + 15.0 * T; h = (b - a) / n; sum = 0.0;
    for (int i = 0; i <= n; i++) {
        E_e = a + i * h;
        p_e = (E_e > m_e) ? sqrt(E_e * E_e - m_e * m_e) : 0.0;
        E_nu = E_e + Qnp;
        val = E_e * p_e * E_nu * E_nu * f_FD(E_e, T) * (1.0 - f_FD(E_nu, T_nu)) * 1.0;
        peso = (i == 0 || i == n) ? 1.0 : ((i % 2 != 0) ? 4.0 : 2.0);
        sum += peso * val;
    }
    sum_total += (sum * h / 3.0);

    // 3. nu + n -> p + e-
    a = Qnp; b = Qnp + 15.0 * T_nu; h = (b - a) / n; sum = 0.0;
    for (int i = 0; i <= n; i++) {
        E_e = a + i * h;
        p_e = sqrt(E_e * E_e - m_e * m_e);
        E_nu = E_e - Qnp;
        val = E_e * p_e * E_nu * E_nu * (1.0 - f_FD(E_e, T)) * f_FD(E_nu, T_nu) * fermi_func(E_e, p_e, 1);
        peso = (i == 0 || i == n) ? 1.0 : ((i % 2 != 0) ? 4.0 : 2.0);
        sum += peso * val;
    }
    sum_total += (sum * h / 3.0);

    return (1.0 / tau_n) * (sum_total / I_vac);
}

double Gamma_pn(double T) {
    if (T < 0.001) return 0.0; 
    double T_nu = T_nu_part(T);
    static double I_vac = 0.0;
    if (I_vac == 0.0) I_vac = I_vac_func();

    double sum_total = 0.0, a, b, h, sum, E_e, p_e, E_nu, val, peso;
    int n = 500;

    // 1. e- + p -> n + nu
    a = Qnp; b = Qnp + 15.0 * T; h = (b - a) / n; sum = 0.0;
    for (int i = 0; i <= n; i++) {
        E_e = a + i * h;
        p_e = sqrt(E_e * E_e - m_e * m_e);
        E_nu = E_e - Qnp;
        val = E_e * p_e * E_nu * E_nu * f_FD(E_e, T) * (1.0 - f_FD(E_nu, T_nu)) * fermi_func(E_e, p_e, 1);
        peso = (i == 0 || i == n) ? 1.0 : ((i % 2 != 0) ? 4.0 : 2.0);
        sum += peso * val;
    }
    sum_total += (sum * h / 3.0);

    // 2. nu_bar + p -> n + e+ (Positron: Fermi = 1.0)
    a = m_e; b = m_e + 15.0 * T_nu; h = (b - a) / n; sum = 0.0;
    for (int i = 0; i <= n; i++) {
        E_e = a + i * h;
        p_e = (E_e > m_e) ? sqrt(E_e * E_e - m_e * m_e) : 0.0;
        E_nu = E_e + Qnp;
        val = E_e * p_e * E_nu * E_nu * (1.0 - f_FD(E_e, T)) * f_FD(E_nu, T_nu) * 1.0;
        peso = (i == 0 || i == n) ? 1.0 : ((i % 2 != 0) ? 4.0 : 2.0);
        sum += peso * val;
    }
    sum_total += (sum * h / 3.0);

    return (1.0 / tau_n) * (sum_total / I_vac);
}

